package com.example;

public class WhileLoop {
    public static void main(String[] args) {
    WhileLoop name = new WhileLoop();
    name.whileExample();
    }

    public void whileExample() {

       int X = 25;
       int Y = 50;

       while (X <= 28)
       {
           System.out.println(X);
           X++;

       }
        System.out.println(Y);

    }

}

